package org.zollty.framework.util.pattern;


/**
 * @author zollty 
 * @since 2013-10-22
 */
public class EqualsMatch extends Pattern {
	
	private final String pattern;
	
	public EqualsMatch(String pattern) {
		this.pattern = pattern;
	}

	@Override
	public String[] match(String str) {
		return pattern.equals(str) ? new String[0] : null;
	}
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((pattern == null) ? 0 : pattern.hashCode());
		return result;
	}
	
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		EqualsMatch other = (EqualsMatch) obj;
		if (pattern == null) {
			if (other.pattern != null)
				return false;
		} else if (!pattern.equals(other.pattern))
			return false;
		return true;
	}

}
